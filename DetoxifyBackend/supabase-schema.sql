-- Detoxify — Supabase schema (single source of truth)
-- Apply in Supabase SQL Editor or via `psql`. Safe to re-run: uses IF NOT EXISTS
-- where possible and DROP POLICY IF EXISTS before policies.
-- =============================================================================
-- Storage (create in Dashboard if missing):
--   - Buckets: post-images, avatars (public read as needed for your app)
